    private void cmdCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCalcularActionPerformed
      int n1 , n2 ,i1,i2, op;
      Complejos f1 ,f2 ,f3 = null;
       
       if (txtReal1.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(this, "Digite la parte real 1","Error", JOptionPane.ERROR_MESSAGE);
           txtReal1.requestFocusInWindow();
       }else if (txtReal2.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(this, "Digite la parte real 2","Error", JOptionPane.ERROR_MESSAGE);
           txtReal2.requestFocusInWindow();
       }else if (txtImaginario1.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(this, "Digite la parte imaginaria 1","Error", JOptionPane.ERROR_MESSAGE);
           txtImaginario1.requestFocusInWindow();
       }else if (txtImaginario2.getText().trim().isEmpty()){
           JOptionPane.showMessageDialog(this, "Digite la parte imaginaria 2","Error", JOptionPane.ERROR_MESSAGE);
           txtImaginario2.requestFocusInWindow();
       }else{
       
       op = cmbOperacion.getSelectedIndex();
       n1 = Integer.parseInt(txtReal1.getText());
       i1= Integer.parseInt (txtImaginario1.getText());
       n2 = Integer.parseInt(txtReal2.getText());
       i2= Integer.parseInt (txtImaginario2.getText());
       
       f1 = new Complejos (n1 , i1);
       f2 = new Complejos (n2 , i2);
       
        switch (op) {
            case 0:
              f3=f1.Suma(f2);
                break;
            case 1:
                f3 = f1.Resta(f2);
                break;
            case 2:
                f3 = f1.Multiplicacion(f2);
                break;
            case 3:
                f3 = f1.Division(f2);
                break;
        }
        
        txtReal3.setText(""+f3.getReal());
        txtImaginario3.setText(""+f3.getImaginaria());
       } 
       
    }//GEN-LAST:event_cmdCalcularActionPerformed

